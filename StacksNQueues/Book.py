class Book:

    def __init__(self, title):
        self.title = title

    def getTitle(self):
        return self.title

    def setTitle(self, title):
        self.title = title

    def setAuthor(self, author):
        self.author = author

    def getAuthor(self):
        return self.author

    def setGenre(self, genre):
        self.genre = genre

    def getGenre(self):
        return self.genre
